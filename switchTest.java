class switchTest
{
    public static void main(String[] args)
    {
        int num = 20;
        switch (num) 
        {
            case 1:
                System.out.println("No is Zero");
                break;
            case 2:
                System.out.println("No is One");
                break;
            default:
                System.out.println("Num");
        }    
    }
}